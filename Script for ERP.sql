CREATE OR REPLACE EDITIONABLE PROCEDURE  "GET_COMPANY_AND_BRANCH_CODE" (
p_username in varchar2, 
p_company_code out varchar2, 
p_branch_code out varchar2 )
is
dummy varchar2(1);
l_emp_id number;
begin
    select 'x' into dummy
    from app_users
    where upper(username) = upper(p_username)
    and user_type = 'S'
    and company_code is null
    and branch_code is null;
    p_company_code := null;
    p_branch_code := null;
exception
    when no_data_found then
        begin
            select company_code,  null into p_company_code, p_branch_code
            from app_users
            where upper(username) = upper(p_username)
            and user_type = 'C'
            and register_user = 1
            and company_code is not null
            and branch_code is null;
        exception
            when no_data_found then
                begin
                    select company_code,  null into p_company_code, p_branch_code
                    from app_users
                    where upper(username) = upper(p_username)
                    and user_type = 'U'
                    and emp_id is null
                    and company_code is not null
                    and branch_code is null;
                exception
                    when no_data_found then
                        begin
                            select emp_id into l_emp_id
                            from app_users
                            where upper(username) = upper(p_username)
                            and user_type = 'U'
                            and emp_id is not null;
                            
                            if l_emp_id is not null then
                                select company_code, branch_code
                                into p_company_code, p_branch_code
                                from emp_info
                                where id = l_emp_id;
                            end if;
                            if p_company_code is null then
                                select company_code into p_company_code
                                from app_users
                                where upper(username) = upper(p_username)
                                and user_type = 'U'
                                and emp_id is not null;
                            end if;
                        exception
                            when no_data_found then
                                p_company_code := null;
                                p_branch_code   := null;
                            when others then
                            null;
                        end;                            
                    when others then
                    null;                    
                end;
            when others then
            null;
        end;
    when others then
    null;
end;
