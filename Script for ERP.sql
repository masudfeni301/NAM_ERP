CREATE OR REPLACE EDITIONABLE FUNCTION  "PASSWORD_MD5" (p_username in varchar2, p_password in varchar2) 
return varchar2 is 
begin 
return dbms_obfuscation_toolkit.md5(input => utl_raw.cast_to_raw(upper(p_username)||'/'||p_password)); 
end password_md5; 
